import com.apptec.registrateapp.repository.localdatabase.RoomHelper;

import org.junit.Test;
import org.junit.runner.RunWith;

import static org.junit.Assert.assertEquals;

/**
 * Instrumented test, which will execute on an Android device.
 *
 * @see <a href="http://d.android.com/tools/testing">Testing documentation</a>
 */
@RunWith(AndroidJUnit4.class)
public class ExampleInstrumentedTest {

    private final String TAG = ExampleInstrumentedTest.class.getSimpleName();

    @Test
    public void useAppContext() {
        // Context of the app under test.
        Context appContext = InstrumentationRegistry.getInstrumentation().getTargetContext();

        assertEquals("com.appTec.RegistrateApp", appContext.getPackageName());

        Log.d(TAG, "Running the test");

        RoomHelper.getAppDatabaseInstance().deviceDao().loadAllDevicesLiveData();
    }
}
